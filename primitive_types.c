#include <stdio.h>

int main() {
    int a = 5;          // integer type
    char b = 'B';       // character type
    float c = 3.14f;    // floating point type
    double d = 9.23;    // double precision floating point type

    printf("Integer a = %d\n", a);
    printf("Char b = %c\n", b);
    printf("Float c = %.2f\n", c);
    printf("Double d = %.2f\n", d);

    return 0;
}
